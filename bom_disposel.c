int magnetValue;
const int upperThreshold = 100;
const int lowerThreshold = -40;

const bool RIGHT = 1;
const bool LEFT = 0;



task init_magnet() {
	int averageMagnet;
	int yolo = 0;  //you only average once

	ClearTimer(T2);
	while(time1(T2) < 1000) {
		averageMagnet += SensorValue[magnet];
		yolo++;
		wait1Msec(10);
	}
	averageMagnet /= yolo;
	//magnet sensor is now calibrated
	while (true) {
		magnetValue = SensorValue[magnet] - averageMagnet;
		wait1Msec(10);
	}
}

task searchForMagnet(){
	bool hasFoundMagnet = false;
	servo[servo1] = 0;
	wait1Msec(1000); //arbitrary number
	int magnetValues[255];

	servoChangeRate[servo1] = 1;
	for (int a = 0; a < 255; a++) {
		magnetValues[a] = magnetValue;
		servo[servo1]++;
		wait1Msec(20);
	}
}


task main() {
	StartTask(init_magnet);
	wait1Msec(1500);

	while (true) {
		if(magnetValue>upperThreshold || magnetValue<lowerThreshold){
			StartTask(searchForMagnet);

		}
		nxtDisplayCenteredTextLine(3, "%i", magnetValue);
		wait1Msec(500);
	}

}
