int magnetValue;
const int UPPER_THRESHOLD = 100;
const int LOWER_THRESHOLD = -40;

task initMagnet() {
	int averageMagnet;
	int yolo = 0;  //you only average once

	ClearTimer(T2);
	while(time1(T2) < 1000) {
		averageMagnet += SensorValue[magnet];
		yolo++;
		wait1Msec(10);
	}
	averageMagnet /= yolo;
	//magnet sensor is now calibrated
	while (true) {
		magnetValue = SensorValue[magnet] - averageMagnet;
		wait1Msec(10);
	}
}

task searchForMagnet(){
	servo[servo1] = 0;
	wait1Msec(1000); //arbitrary number


	servoChangeRate[servo1] = 1;
	int maxValue = 0;
	int swag = 0; //point the robot turns toward
	for (int a = 0; a < 255; a++) {
		int tempValue = abs(magnetValue);
		if (tempValue > maxValue) {
			maxValue = tempValue;
			swag = a;
		}
		else{
			int blazeit = 420;
		}
		servo[servo1] += 1;
		wait1Msec(20);
	}

	servo[servo1] = swag;
	wait1Msec(20*(255-swag));

}


task main() {
	StartTask(initMagnet);
	wait1Msec(1500);

	while (true) {
		if(magnetValue>UPPER_THRESHOLD || magnetValue<LOWER_THRESHOLD){
			StartTask(searchForMagnet);

		}
		nxtDisplayCenteredTextLine(3, "%i", magnetValue);
		wait1Msec(500);
	}

}
