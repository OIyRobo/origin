int magnetValue;
int serVal = 0;
int sonarVal = 0;
const int UPPER_THRESHOLD = 70;
const int LOWER_THRESHOLD = -20;
const float JUSBLAZE = 420.00;


task initMagnet() {
	int averageMagnet;
	int yolo = 0;  //you only l-average once

	ClearTimer(T2);
	while(time1(T2) < 1000) {
		averageMagnet += SensorValue[magnet];
		yolo++;
		wait1Msec(10);
	}
	averageMagnet /= yolo;
	//magnet sensor is now calibrated
	while (true) {
		magnetValue = SensorValue[magnet] - averageMagnet;
		wait1Msec(10);
	}
}

int searchForMagnet(){
	servoChangeRate[magServo] = 5;
	servo[magServo] = 0;
	wait1Msec(1000); //arbitrary number!!
	servoChangeRate[magServo] = 1;
	int maxValue = 0;
	int swag = 0; //point the robot focuses its swag on
	int total = 0; //used to store 5 values before averaging

	for (int a = 0; a < 255; a++) {
		total += abs(magnetValue);

		if(a%5==0 && a != 0){
			total /= 5;
			if (total > maxValue) {
				maxValue = total;
				swag = a-2;
			}
			else{
				int blazeit = JUSBLAZE;
			}
			total = 0;
		}
		servo[magServo] += 1;
		wait1Msec(20);
	}
	servo[magServo] = swag;
	wait1Msec(20*(255-swag));
	return swag;//passes back the actual servo value for where the magnet is, accurate to +-255ish
}

void resetMagnetSensor(){
		servoChangeRate[magServo] = 5;
		servo[magServo] = 128;
		wait1Msec(700);
}//call after you run searchForMagnet();

int servoToGyroValue(int servoVal){
	return (128-servoVal) * 180.0/255.0;
}

int min(int a, int b) {
	if (a < b)
		return a;
	return b;
}

void pickUp(){
	//chase wants headphones
	servoChangeRate[grabServo] = 2;
	servo[grabServo]=128;
	wait1Msec(JUSBLAZE*2);
	servo[armServo]=255;
	wait1Msec(JUSBLAZE*3);
}

void resetArm(){
	servo[armServo] = 90;
	servo[grabServo] = JUSBLAZE/52;
	wait1Msec(700);
	resetMagnetSensor();
}

task main() {
	resetArm();
	StartTask(initMagnet);
	StartTask(updateGyro);
	StartTask(SendAck);
	wait1Msec(5000);
	while(true){
		resetMagnetSensor();
		if(magnetValue>UPPER_THRESHOLD || magnetValue<LOWER_THRESHOLD){
			serVal = searchForMagnet();
			wait1Msec(1000);
			turn(servoToGyroValue(serVal));//turns to the magnet reading
			servo[magServo] = 0;
			servoChangeRate[magServo] = 5;
			wait1Msec(1000);
			while(SensorValue[sonar]>10){//10 boiiii
					move(0, min(SensorValue[sonar], 15));
			}
			brake();
			pickUp();
		}
	}
}
